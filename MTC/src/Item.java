
public class Item {
	
	String title;
	String link;
	String description;
	String enclosure;
	String guid;
	String pubDate;
	String source;
	
	public Item(String title, String link, String description, String enclosure, String guid, String pubDate,
			String source) {

		this.title = title;
		this.link = link;
		this.description = description;
		this.enclosure = enclosure;
		this.guid = guid;
		this.pubDate = pubDate;
		this.source = source;
	}
	
	

}
